# Функция для расчета суммы вклада спустя Y лет с учетом 10% годовых
def bank(X, Y):
    for _ in range(Y):
        X += X * 0.10  # Увеличиваем вклад на 10% каждый год
    return X

# Ввод начальной суммы вклада и количества лет
X = float(input("Введите начальную сумму вклада (в рублях): "))
Y = int(input("Введите количество лет: "))

# Вызов функции и вывод результата
final_amount = bank(X, Y)
print(f"Сумма на счету спустя {Y} лет: {final_amount:.2f} руб.")