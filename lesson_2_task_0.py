# Запрашиваем возраст пользователя через input и сохраняем в переменную my_age
my_age = input("Введите ваш возраст: ")

# Преобразуем строку в число
my_age = int(my_age)

# Выводим возраст на экран
print("Ваш возраст:", my_age)

# Увеличиваем возраст на 1 и сохраняем в переменную
my_age = my_age + 1

# Выводим обновленный возраст
print("Ваш обновленный возраст:", my_age)